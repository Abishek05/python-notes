# Mutable
list_1 = ['Physics', 'Chemistry', 'Maths', 'Biology']
list_2 = list_1

print(list_1)
print(list_2)

list_1[0] = 'Electronics'

print(list_1)
print(list_2)

# Immutable
tuple_1 = ('Physics', 'Chemistry', 'Maths', 'Biology')
tuple_2 = tuple_1

print(tuple_1)
print(tuple_2)

tuple_1[0] = 'Electronics'

print(tuple_1)
print(tuple_2)

