courses = ['Physics', 'Chemistry', 'Maths', 'Biology']

print(courses)
print(courses[3])   # Access value at 4
print(courses[-1])   # Access value at 4, i.e last item
print(courses[0:2])   # Access range of items
print(courses[:2])   # Assume we want to start from the beginning
print(courses[2:])   # Start at 2 until the last
